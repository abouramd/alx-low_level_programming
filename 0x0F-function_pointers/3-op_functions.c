#include "3-calc.h"

/**
 * op_add - function operater
 * @a: first num
 * @b: second num
 * Return: an integer the result of operation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function operater
 * @a: first num
 * @b: second num
 * Return: an integer the result of operation
 */

int op_sub(int a, int b)
{
	return (a - b);
}

